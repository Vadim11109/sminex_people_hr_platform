export interface GradeInfo {
  label: string
  grade: 'S' | 'M' | 'J' | 'N'
  rank: number
  cls: string   // CSS class for grade badge background
  fcls: string  // CSS class for progress fill
}

// ─── GRADE LOGIC ─────────────────────────────────────────────────────────────

export function getGradeInfo(avg: number): GradeInfo {
  const grade: GradeInfo['grade'] = avg >= 2.4 ? 'S' : avg >= 1.6 ? 'M' : avg >= 0.8 ? 'J' : 'N'
  const cls  = { S: 'grade-s',  M: 'grade-m',  J: 'grade-j',  N: 'grade-nd' }[grade]
  const fcls = { S: 'fill-s',   M: 'fill-m',   J: 'fill-j',   N: 'fill-nd'  }[grade]
  let label: string, rank: number
  if      (avg >= 2.8)  { label = 'Сеньор Ранг 3';  rank = 3 }
  else if (avg >= 2.6)  { label = 'Сеньор Ранг 2';  rank = 2 }
  else if (avg >= 2.4)  { label = 'Сеньор Ранг 1';  rank = 1 }
  else if (avg >= 2.13) { label = 'Мидл Ранг 3';    rank = 3 }
  else if (avg >= 1.87) { label = 'Мидл Ранг 2';    rank = 2 }
  else if (avg >= 1.6)  { label = 'Мидл Ранг 1';    rank = 1 }
  else if (avg >= 1.33) { label = 'Джуниор Ранг 3'; rank = 3 }
  else if (avg >= 1.07) { label = 'Джуниор Ранг 2'; rank = 2 }
  else if (avg >= 0.8)  { label = 'Джуниор Ранг 1'; rank = 1 }
  else                  { label = 'Ниже Джуниора';  rank = 0 }
  return { label, cls, fcls, grade, rank }
}

export function getSummaryText(avg: number, ratedCount: number): string {
  if (ratedCount < 9) return ''
  const { grade, rank } = getGradeInfo(avg)
  if (grade === 'S') {
    if (rank === 3) return 'Сеньор Ранг 3 — эталонный Senior. Формирует продуктовую повестку и стратегию на уровне направления. Готов к роли Product Lead или ментора команды.'
    if (rank === 2) return 'Сеньор Ранг 2 — зрелый Senior. Уверенно ведёт продукт, управляет через метрики, влияет на бизнес-решения. До Ранга 3 — системность и кросс-командное лидерство.'
    return 'Сеньор Ранг 1 — входящий Senior. Компетенции на Senior-уровне, нужна бо́льшая стабильность и масштаб влияния. Рост внутри грейда через стратегические проекты.'
  }
  if (grade === 'M') {
    if (rank === 3) return 'Мидл Ранг 3 — сильный Middle на пороге Senior. Самостоятельно ведёт продукт, работает с бизнесом и командой. Точечный рост в 2–3 зонах откроет путь к Сеньор Ранг 1.'
    if (rank === 2) return 'Мидл Ранг 2 — уверенный Middle. Хорошо освоены ключевые процессы, есть самостоятельность в приоритизации. Рост — в глубине аналитики и работе со стейкхолдерами.'
    return 'Мидл Ранг 1 — переходный уровень Middle. Базовые процессы освоены, требуется усиление самостоятельности. Рекомендуется план развития на 3–6 месяцев.'
  }
  if (grade === 'J') {
    if (rank === 3) return 'Джуниор Ранг 3 — зрелый Junior. Уверенно работает в рамках процессов, начинает проявлять самостоятельность. Прицел на переход в Мидл через 3–6 месяцев при правильном фокусе.'
    if (rank === 2) return 'Джуниор Ранг 2 — Junior в развитии. Осваивает инструменты и процессы, выполняет задачи с поддержкой. Рост — в снижении зависимости от руководителя.'
    return 'Джуниор Ранг 1 — начало пути. Задачи выполняются по заданному контексту. Требуется структурная поддержка и обучение базовым компетенциям PO.'
  }
  return 'Ниже уровня Джуниора. Рекомендуется базовое обучение и повторный ассесмент через 2–3 месяца.'
}

